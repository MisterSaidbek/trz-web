                    ОНИ СРАЖАЛИСЬ ЗА РОДИНУ
                    1555 заводчан, ушедших на фронт для защиты родной земли, из которых 33 – женщины, проливали кровь на полях сражений. Многие, кому посчастливилось остаться в живых и вернуться, это люди, которые на своих плечах вынесли все тяготы войны, им не надо ничего объяснять и доказывать, они были там, они знают о войне не понаслышке. К сожалению, время неумолимо и ветераны уходят от нас в бессмертие, и мы вспоминаем их имена с глубокой благодарностью. Это - героическое поколение победителей, благодаря которым мы живём и трудимся в мире и согласии.
                    Навечно зачислен в списки рабочих механического цеха гвардии генерал майор, Герой Советского Союза Сабир Рахимов.
                    Возле инструментально-механического цеха установлен обновлённый бюст бесстрашному сыну узбекского народа, первому узбекскому генералу Сабиру Рахимову, погибшему на поле сражений с фашистскими агрессорами в 1945 году. Из воспоминаний и архивных документов, бережно сохраняемых в музее истории нашего завода, мы знаем, что работники завода решили увековечить память земляка и навечно зачислить его в списки рабочих своего предприятия. В механическом цехе стоял станок № 3 (ДИП - 500), который был закреплён за Героем Великой Отечественной войны Сабиром Рахимовым и право трудиться за этим станком было предоставлено одному из лучших производственников цеха Герою труда Абдушукуру Кадырову.
                    Рабочие завода гордятся подвигами П.Ф. Чупикова.  В 30-е годы десятки молодых рабочих завода ушли учиться в училище аэрофлота. В их числе был модельщик П.Ф. Чупиков. Став летчиком-истребителем, он еще в 1938 году отличился в боях с японцами. В годы Второй мировой войны П.Ф. Чупиков командовал авиаполком, который особенно прославился во время грандиозной битвы на Курской дуге. В 1944 году П.Ф. Чупикову было присвоено звание Героя Советского Союза.
