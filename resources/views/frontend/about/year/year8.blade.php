                    Азимов Таджибай, 1919 года рождения, сержант.
                    В ноябре 1939 года был призван на срочную военную службу в армию, затем прошел фронтовые дороги до самого Дня Победы на 3-ем Белорусском фронте. Участвовал в обороне города Ленинграда, в освобождении Польши, Чехословакии, Германии.  Удостоен медалями «За оборону Ленинграда», «За оборону Москвы», «За отвагу», «20 лет Победы над Германией», «25 лет Победы в Великой Отечественной войне», «50 лет Советской армии и флота»
                    Абдураимов Махмуд, 1919 года рождения, сержант.
                    Война застала его в Белоруссии, где он служил с 1939 года в армии в зенитном артиллерийском полку. В октябре 1941 года он был назначен командиром отделения 734-го зенитного артиллерийского полка на 1-ом Белорусском фронте, где и провоевал до Дня Победы. За проявленную храбрость в боях был удостоен орденом Красной Звезды, медалями «За оборону Кавказа», «За Победу над Германией», «20 лет Победы над Германией», «50 лет Советской армии и флота»
                    Акрамов Аббас Умарович, 1916 года рождения, рядовой.
