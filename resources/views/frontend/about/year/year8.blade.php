                    Донец Владимир Степанович 1915 года рождения, младший лейтенант. Командовал взводом на Северо-Западном фронте в 11-Армии 43-Гвардейской Латышской дивизии с февраля 1942 года по 1946 год. Удостоен орденом Красной Звезды, медалями «За Победу над Германией», «20 лет Победы над Германией», «25 лет Победы в Великой Отечественный войне», «50 лет Советской армии и флота»
